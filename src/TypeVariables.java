public class TypeVariables {
    public static void main(String[] args) {
        double value = 2550D;
        System.out.println("About double: " + value);

        int number = 2;
        number = 10;

        System.out.println(number);

        final double VALUE_OF_PI = 3.14;

        System.out.println("Constant values do not change: " + VALUE_OF_PI);
    }
}
